# Create a list Using []

A = [0, 5 , 15, 7] # A is Container And it Include The Values

# Print The List Using print() Function 
print(A)

# Access The Index Using A[0], A[1], A[2], Etc (It is like a String Slicing)
print(A[0])  

# Change The Value Of List Using 
A[0] = 1
print(A)

# We Can Create A list With Different Types Of Item
D = [1, "SANCHIT", True, None, 0.1]
print(D)

# List Slicing 

friends = ["OM", "RAM", "SAI", "SHAM", "RAVI", "DEV"]
print(friends[0:4])